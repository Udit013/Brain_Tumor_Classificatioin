#!/usr/bin/env python3
"""Inject MEASURED results into the README between marker comments.

This is the ONLY path by which numbers enter the README's production-results
table. It reads results/metrics/*.json (produced by an actual run) and replaces
the block between:

    <!-- RESULTS:BEGIN -->
    ... (auto-generated, do not edit by hand) ...
    <!-- RESULTS:END -->

If a metrics file is missing, the corresponding row stays "PENDING RUN" — so the
README can never claim a number that was not computed on this machine.

Usage:
    python scripts/fill_readme_results.py
"""

from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
METRICS = ROOT / "results" / "metrics"
README = ROOT / "README.md"
BEGIN = "<!-- RESULTS:BEGIN -->"
END = "<!-- RESULTS:END -->"


def _load(name):
    p = METRICS / name
    if not p.exists():
        return None
    return json.loads(p.read_text())


def _fmt(x, pct=False, nd=4):
    if x is None:
        return "_PENDING RUN_"
    return f"{x*100:.3f}%" if pct else f"{x:.{nd}f}"


def build_block() -> str:
    """Emit the portfolio results table — exactly nine rows, all real numbers.

    Every value is read from results/metrics/*.json (produced by an actual run).
    If any required metrics file is missing this raises, rather than emit a
    placeholder — the portfolio README must never ship a _PENDING_ cell.
    """
    ev = _load("evaluation.json")
    lk = _load("leakage.json")
    ex = _load("external_validation.json")
    cal = _load("calibration.json")
    lat = _load("latency.json")
    missing = [n for n, d in {
        "evaluation": ev, "leakage": lk, "external_validation": ex,
        "calibration": cal, "latency": lat}.items() if d is None]
    if missing:
        raise SystemExit(
            f"Missing metrics: {missing}. Run scripts/reproduce.sh first; "
            "the README table is not generated from partial results.")

    keras = lat["keras"]["mean_ms"]
    cpu = lat["onnx"]["CPUExecutionProvider"]["mean_ms"]
    coreml = lat["onnx"]["CoreMLExecutionProvider"]["mean_ms"]
    bc = ex["binary_collapse"]

    rows = [
        ("Reproduced accuracy (full test set)", f"{ev['accuracy']*100:.2f}%"),
        ("Published reference (paper)", "99.844%"),
        ("Macro ROC-AUC / avg-precision",
         f"{ev['macro_roc_auc']:.3f} / {ev['macro_avg_precision']:.3f}"),
        ("Exact cross-split duplicates",
         f"{lk['exact_duplicates_in_test']} / {lk['n_test']} "
         f"({lk['exact_duplicate_rate']*100:.1f}%)"),
        (f"Near-duplicates (phash≤{lk['phash_near_dup_threshold']})",
         f"{lk['near_duplicates_in_test']} / {lk['n_test']} "
         f"({lk['near_duplicate_rate']*100:.1f}%)"),
        ("Accuracy on near-duplicate vs leak-free images",
         f"{lk['accuracy_near_dup_subset']*100:.1f}% vs "
         f"{lk['accuracy_leak_free_subset']*100:.1f}%"),
        (f"External OOD accuracy (binary collapse, n={bc['n']})",
         f"{bc['accuracy']*100:.1f}%"),
        ("ECE before → after temperature scaling",
         f"{cal['ece_before']:.4f} → {cal['ece_after']:.4f} "
         f"(T={cal['temperature']:.3f})"),
        ("Inference latency, Keras / ONNX-CPU / ONNX-CoreML",
         f"{keras:.0f}ms / {cpu:.0f}ms / {coreml:.1f}ms"),
    ]

    lines = [
        "> Auto-generated by `scripts/fill_readme_results.py` from "
        "`results/metrics/*.json` produced by `scripts/reproduce.sh`. "
        "Every value is measured — no placeholders.\n",
        "| Metric | Value |",
        "|---|---|",
    ]
    lines += [f"| {m} | {v} |" for m, v in rows]
    return "\n".join(lines)


def main() -> None:
    block = build_block()
    text = README.read_text()
    if BEGIN not in text or END not in text:
        raise SystemExit(f"Markers {BEGIN}/{END} not found in README.md")
    pre = text.split(BEGIN)[0]
    post = text.split(END)[1]
    new = f"{pre}{BEGIN}\n{block}\n{END}{post}"
    README.write_text(new)
    print("README results table updated from measured metrics.")


if __name__ == "__main__":
    main()
